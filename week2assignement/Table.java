package week2assignement;

public class Table {

	public static void main(String[] args) {
		// intitalize the value
		int i;

		// intitalize num for the table which we want to print
		int num = 10;
		// increment the value using for loop from 1 to the numbwer we want
		for (i = 1; i <= num; i++)

		{

			System.out.println(i + "*" + num + "=" + i * num);

		}

	}

}
